#!/usr/bin/env python3
# The concatenation operator (+)
first = "Casey"
last = "Jackson"
full = first + " " + last
print(full) # Casey Jackson

fullName = "Casey" " " "Jackson"
print(fullName)

# The asterisk (*) operator
stars = "*" * 12
pounds = 5 * "@"
print(stars, ":", pounds) # ************ : @@@@@

# The in operator is convenient for membership tests
x = "Hello there"
print('t' in x, 'ell' in x, 'hell' in x) # True True False
